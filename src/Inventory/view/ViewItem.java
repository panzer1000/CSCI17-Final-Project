    private void btnEditItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditItemActionPerformed
        // TODO add your handling code here:
        EditItem editItem = new EditItem(this,true);
        
        int row = tblItems.getSelectedRow();
        editItem.setItemCode(tblItems.getValueAt(row, 0).toString());
        editItem.setItemName(tblItems.getValueAt(row, 1).toString());
        editItem.setItemDescription(tblItems.getValueAt(row, 2).toString());
        editItem.setItemQuantity(tblItems.getValueAt(row, 3).toString());
        editItem.setItemUOM(tblItems.getValueAt(row, 4).toString());
        editItem.setItemCategory(tblItems.getValueAt(row, 5).toString());
        editItem.setItemCost(tblItems.getValueAt(row, 6).toString());
        
        editItem.setVisible(true);
        
        
        
    }//GEN-LAST:event_btnEditItemActionPerformed

