    private void populateTable(){
        try{
            ps = conn.prepareStatement("SELECT ItemID, Name, Description, Quantity, UnitOfMeasure"
                    + ", Category, UnitPrice FROM tblAddItem");
            rs = ps.executeQuery();
            
            tblItems.setModel(DbUtils.resultSetToTableModel(rs));
            
            ps.close();
            rs.close();
        }catch(Exception ex){
            System.out.println(ex);
        }
         
    }
    
